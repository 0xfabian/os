void ls(const char* path)
{
    kprintf("> ls %s\n", path);

    auto dir = File::open(path, 0);

    if (!dir)
    {
        kprintf("Failed to open `%s`: %d\n", path, dir.error());
        return;
    }

    char buf[256];
    while (dir->iterate(buf, sizeof(buf)) > 0)
        kprintf("%s  ", buf);

    kprintf("\n");

    dir->close();
}

